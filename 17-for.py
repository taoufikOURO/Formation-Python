#la boucle for
#parcourir une liste
notes = [10, 12, 11, 14, 17, 13, 12, 19, 14]
for note in notes:
    print(note)
    

#parcourir une chaine de caractere
cours = "Python"
for l in cours:
    print(l)

#la fonction range
#repeter une action une certaine nombre de fois
for i in range(5):
    print("Bonjour")